/**
 * Допустимые типы входного значения.
 */
export type NumericInput = number | string | null | undefined

/**
 * Функция-преобразователь числа.
 * Вызывается уже после нормализации входных данных:
 *   - null -> 0
 *   - строка -> number (если получается распарсить)
 */
export type NumericTransform = (value: number) => number

/**
 * Функция, которая решает, какой знак показывать.
 *
 * Должна вернуть:
 *   - "+"  для положительных значений
 *   - "-"  для отрицательных
 *   - ""   если знак не нужен
 *
 * На вход подаётся ЧИСЛО ПОСЛЕ transform/округления.
 */
export type SignResolver = (value: number) => string

/**
 * Базовые опции форматирования.
 * Общие для обоих режимов: строка и число.
 */
export interface NumericFormatBaseOptions {
  /**
   * Количество знаков после запятой при округлении.
   *
   * По умолчанию: 0 (округление до целого).
   * Игнорируется, если передан колбэк transform.
   */
  fractionDigits?: number

  /**
   * Разделитель дробной части в результирующей СТРОКЕ.
   *
   * По умолчанию: ",".
   * Пример: 1234.5 -> "1 234,5"
   */
  decimalSeparator?: string

  /**
   * Разделитель групп разрядов в результирующей СТРОКЕ.
   *
   * По умолчанию: пробел " ".
   * Например: "1234567" -> "1 234 567".
   * Если передать пустую строку "", группировка выполняться не будет.
   */
  thousandsSeparator?: string

  /**
   * Колбэк для произвольного преобразования числа.
   *
   * ВАЖНО:
   * - Вызывается уже после нормализации входного значения.
   * - Если указан transform, параметр fractionDigits полностью игнорируется.
   *   Ответственность за округление / точность лежит на колбэке.
   */
  transform?: NumericTransform

  /**
   * Нормализовать запятую как десятичный разделитель во ВХОДНОЙ строке.
   *
   * По умолчанию: true.
   * Например: "1 234,56" -> "1234.56" перед парсингом.
   */
  normalizeCommaAsDecimal?: boolean

  /**
   * Удалять пробельные символы во ВХОДНОЙ строке.
   *
   * По умолчанию: true.
   * Например: "  1 234,56 " -> "1234,56" перед дальнейшей обработкой.
   */
  stripWhitespace?: boolean

  /**
   * Добавлять ли знак в строковом результате.
   *
   * По умолчанию: false
   *
   * При false поведение как раньше:
   *   - отрицательные → "-123"
   *   - положительные → "123"
   *   - ноль         → "0"
   *
   * При true знак формируется через signResolver.
   * Работает только если as !== "number".
   */
  includeSign?: boolean

  /**
   * Кастомный резолвер знака. Работает только если includeSign === true.
   *
   * По умолчанию:
   *   v > 0  → "+"
   *   v < 0  → "-"
   *   v === 0 → ""
   */
  signResolver?: SignResolver
}

/**
 * Опции для режима, когда результатом должна быть СТРОКА.
 * Это режим по умолчанию.
 */
export type NumericFormatStringOptions = {
  /**
   * Явное указание типа результата.
   * Если не указано или равно "string" — результат всегда string.
   */
  as?: 'string'
} & NumericFormatBaseOptions

/**
 * Опции для режима, когда результатом должно быть ЧИСЛО.
 */
export type NumericFormatNumberOptions = {
  /**
   * В этом режиме функция вернёт number.
   */
  as: 'number'
} & NumericFormatBaseOptions

/**
 * Объединённый тип опций (для удобства экспорта).
 */
export type NumericFormatOptions
  = | NumericFormatStringOptions
    | NumericFormatNumberOptions
